<p><strong><font color="#0000CC" size="2" face="Verdana, Arial, Helvetica, sans-serif">Para 
  el llenado de las consultas de urgencia:</font></strong></p>
<ol>
  <li><font color="#0000CC" size="2" face="Verdana, Arial, Helvetica, sans-serif">Una 
    vez que tenga todos los &#8220;datos de urgencia&#8221;, sep&aacute;relos 
    en 3 grupos: respiratorios, quir&uacute;rgicos y m&eacute;dicas. </font></li>
  <li><font color="#0000CC" size="2" face="Verdana, Arial, Helvetica, sans-serif"> 
    Estos 3 grupos los debe subdividir seg&uacute;n los grupos et&aacute;reos 
    que se requieren en el informe. </font></li>
  <li><font color="#0000CC" size="2" face="Verdana, Arial, Helvetica, sans-serif"> 
    Digitar en los campos:</font></li>
</ol>
<ul>
  <li><font color="#0000CC" size="2" face="Verdana, Arial, Helvetica, sans-serif"> 
    Bronquitis, </font></li>
  <li><font color="#0000CC" size="2" face="Verdana, Arial, Helvetica, sans-serif">asma,</font></li>
  <li><font color="#0000CC" size="2" face="Verdana, Arial, Helvetica, sans-serif"> 
    neumon&iacute;a influenza,</font></li>
  <li><font color="#0000CC" size="2" face="Verdana, Arial, Helvetica, sans-serif"> 
    laringitis, </font></li>
  <li><font color="#0000CC" size="2" face="Verdana, Arial, Helvetica, sans-serif">resto 
    respiratorias, </font></li>
  <li><font color="#0000CC" size="2" face="Verdana, Arial, Helvetica, sans-serif">total 
    quir&uacute;rgicas (los SAPUS no deber&iacute;an llenar esta celda) y </font></li>
  <li><font color="#0000CC" size="2" face="Verdana, Arial, Helvetica, sans-serif">total 
    m&eacute;dicas o resto de las m&eacute;dicas, sin incluir las respiratorias. 
    </font></li>
</ul>
<p><font color="#0000CC" size="2" face="Verdana, Arial, Helvetica, sans-serif">Nota: 
  La suma de todas las urgencias es = total m&eacute;dicas(ingresadas directamente) 
  + total quir&uacute;rgicas(ingresadas directamente) + total respiratorias(autom&aacute;ticamente 
  como la suma de bronq.+asma+neumo.+laring.+resto resp.)</font></p>
<p><font color="#0000CC" size="2" face="Verdana, Arial, Helvetica, sans-serif"><strong>Observaciones 
  generales:</strong></font></p>
<ul>
  <li><font color="#0000CC" size="2" face="Verdana, Arial, Helvetica, sans-serif">Las 
    filas o columnas<strong> achuradas (con color gris)</strong> indican que no 
    pueden anotarse datos en ellas, s&oacute;lo reflejan sumatorias de las otras 
    celdas.</font></li>
  <li><font color="#0000CC" size="2" face="Verdana, Arial, Helvetica, sans-serif">A 
    medida que ingrese los datos, se ir&aacute;n completando autom&aacute;ticamente 
    las celdas de los subtotales y totales.</font></li>
  <li><font color="#0000CC" size="2" face="Verdana, Arial, Helvetica, sans-serif">Para 
    grabar la informaci&oacute;n, debe pulsar expl&iacute;citamente el <strong>bot&oacute;n 
    &quot;Grabar&quot;</strong>, situado al fondo del formulario</font></li>
  <li><font color="#0000CC" size="2" face="Verdana, Arial, Helvetica, sans-serif">Si 
    se<strong> equivoc&oacute; al ingresar los datos</strong>, y se percat&oacute; 
    de ello despu&eacute;s de grabar, no se preocupe por cuanto puede corregirlo 
    con la opci&oacute;n &quot;Editar&quot; de la p&aacute;gina que aparece cuando 
    Ud. elige Registrar. </font></li>
  <li><font color="#0000CC" size="2" face="Verdana, Arial, Helvetica, sans-serif">Y 
    m&aacute;s dr&aacute;sticamente, si se requiere, puede eliminar el formulario 
    de ese d&iacute;a con la opci&oacute;n &quot;Eliminar&quot; de la p&aacute;gina 
    que aparece cuando Ud. elige Registrar.</font></li>
</ul>
